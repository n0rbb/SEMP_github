int absolute(int16_t value){
	if (value >= 0) return value;
	else return -value;
}
void read_Accel(){
	tim10finished = 0;
	BSP_ACCELERO_GetXYZ(xyz);

	if (absolute(xyz[0]) > threshold_x){
		Green_Led_state = 1;
	}
	if (absolute(xyz[1]) > threshold_y){
			Orange_Led_state = 1;
	}
	if (absolute(xyz[2]) > threshold_z){
			Red_Led_state = 1;
	}

}

